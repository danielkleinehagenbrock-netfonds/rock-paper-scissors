package de.netfonds.rockpaperscissors.game;

/**
 * Enumeration of hand shapes for the default Rock Paper Scissors game.
 *
 * <p>Represents the three classic hand shapes with their traditional meanings.
 * Each enum constant implements the {@link HandShape} marker interface.
 */
public enum ClassicHandShape implements HandShape {
    ROCK,
    PAPER,
    SCISSORS
}
